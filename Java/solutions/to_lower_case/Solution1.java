package solutions.to_lower_case;

public class Solution1 {
    public String toLowerCase (String s){
        return s.toLowerCase();
    }
}
//time ve sapace O(n)

/*String.toLowerCase() metodu, Java Geliştirme Kiti'nin (JDK) bir parçasıdır ve genellikle C/C++ gibi daha düşük seviyeli dillerde yazılmış, platforma özel optimizasyonlar içerir. Bu nedenle, Solution2'deki manuel döngüye göre genellikle daha iyi bir ham performansa sahip olur */